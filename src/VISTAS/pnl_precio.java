    void limpiar() {
        txt_id.setText("");
        txt_precio_adulto.setText("");
        txt_infantil.setText("");
        txt_nombre_precio.setText("");
    }

    void listar(String dato) {
        try {
            modeloP = omPrecio.ListarPrecio(dato);
            tabla.setModel(modeloP);
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "error al listar los precios");
        }
    }

    void insertar() {
        try {
            if (txt_precio_adulto.getText().isEmpty() || txt_infantil.getText().isEmpty()) {

                JOptionPane.showConfirmDialog(null, "complete los campos");
            } else {
                omPrecio.insertarPrecio(Double.parseDouble(txt_precio_adulto.getText()), Double.parseDouble(txt_infantil.getText()), txt_nombre_precio.getText());
                limpiar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error al insertar datos cliente");
        }
    }

    void recuperar_usuario() {
        int select = tabla.getSelectedRow();
        txt_id.setText(modeloP.getValueAt(select, 0).toString());
        txt_precio_adulto.setText(modeloP.getValueAt(select, 1).toString());
        txt_infantil.setText(modeloP.getValueAt(select, 2).toString());
        txt_nombre_precio.setText(modeloP.getValueAt(select, 3).toString());

    }

    void modificar() {
        try {
            if (txt_id.getText().isEmpty() || txt_precio_adulto.getText().isEmpty() || txt_infantil.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "complete los campos");
            } else {
                omPrecio.modificarPrecio(txt_id.getText(), Double.parseDouble(txt_precio_adulto.getText()), Double.parseDouble(txt_infantil.getText()),txt_nombre_precio.getText());
                limpiar();
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "ocurrio un error al modificar");
        }
    }

    void eliminar() {
        try {

            int sselect = tabla.getSelectedRow();
            if (sselect >= 0) {
                omPrecio.eliminarPrecio(modeloP.getValueAt(sselect, 0).toString());
            } else {

                JOptionPane.showMessageDialog(null, "selecione para eliminar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error al eliminar los precios");
        }
    }

